/**
 * Fetches covers for books that still have no cover_url, using Google Books API.
 * Outputs: supabase/migrations/0014_missing_covers.sql
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const env = Object.fromEntries(
  readFileSync(resolve(__dir, '../.env'), 'utf8')
    .split('\n').filter(l => l.includes('='))
    .map(l => l.split('=').map(s => s.trim()))
);

const supabase = createClient(env['VITE_SUPABASE_URL'], env['VITE_SUPABASE_ANON_KEY']);
const API_KEY  = env['GOOGLE_BOOKS_API_KEY'];
const sleep    = ms => new Promise(r => setTimeout(r, ms));

function esc(str) {
  return `'${String(str).replace(/'/g, "''")}'`;
}

async function fetchGoogleBooksCover(title, author) {
  const q = encodeURIComponent(`intitle:${title} inauthor:${author}`);
  const url = `https://www.googleapis.com/books/v1/volumes?q=${q}&maxResults=3&key=${API_KEY}`;

  const res = await fetch(url);
  if (!res.ok) { console.error(`  HTTP ${res.status}`); return null; }
  const data = await res.json();

  const items = data.items || [];
  const tLower = title.toLowerCase();
  const item = items.find(i => i.volumeInfo?.title?.toLowerCase().includes(tLower.slice(0, 8)))
    || items[0];

  if (!item) return null;

  let thumb = item.volumeInfo?.imageLinks?.thumbnail
    || item.volumeInfo?.imageLinks?.smallThumbnail
    || null;

  if (thumb) {
    thumb = thumb.replace('http://', 'https://').replace(/zoom=\d/, 'zoom=2').replace('&edge=curl', '');
  }

  return thumb;
}

async function main() {
  // Only books still missing a cover
  const { data: books, error } = await supabase
    .from('books')
    .select(`slug, title, books_authors ( position, authors ( display_name ) )`)
    .eq('is_published', true)
    .is('cover_url', null);

  if (error) { console.error(error.message); process.exit(1); }
  if (!books.length) { console.log('All books already have covers!'); return; }

  console.log(`\nFetching covers for ${books.length} books via Google Books API…\n`);

  const lines = [
    '-- Auto-generated by scripts/enrich-missing-covers.mjs',
    '-- Adds cover_url for books that Open Library did not cover.',
    '',
  ];

  let found = 0;

  for (const book of books) {
    const authorEntry = [...(book.books_authors || [])]
      .sort((a, b) => (a.position ?? 1) - (b.position ?? 1))[0];
    const author = authorEntry?.authors?.display_name || '';

    process.stdout.write(`  ${book.title.padEnd(50).slice(0, 50)} `);

    const cover = await fetchGoogleBooksCover(book.title, author);

    if (cover) {
      lines.push(`UPDATE public.books SET cover_url = ${esc(cover)} WHERE slug = ${esc(book.slug)};`);
      found++;
      console.log(`✓ cover`);
    } else {
      console.log(`✗ still not found`);
    }

    await sleep(350);
  }

  const outPath = resolve(__dir, '../supabase/migrations/0014_missing_covers.sql');
  writeFileSync(outPath, lines.join('\n'), 'utf8');

  console.log(`\n─────────────────────────────────`);
  console.log(`Covers found : ${found} / ${books.length}`);
  console.log(`Written  → supabase/migrations/0014_missing_covers.sql`);
  console.log(`Next     : supabase db push`);
}

main().catch(e => { console.error(e); process.exit(1); });
