/**
 * Metadata enrichment — page count, ISBN, publisher, language, pub year
 * Source: Open Library search API (free, no key)
 *
 * Run:  node scripts/enrich-metadata.mjs
 * Then: supabase db push
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const env = Object.fromEntries(
  readFileSync(resolve(__dir, '../.env'), 'utf8')
    .split('\n').filter(l => l.includes('='))
    .map(l => l.split('=').map(s => s.trim()))
);

const supabase = createClient(env['VITE_SUPABASE_URL'], env['VITE_SUPABASE_ANON_KEY']);
const sleep = ms => new Promise(r => setTimeout(r, ms));

function esc(str) {
  if (!str && str !== 0) return 'NULL';
  return `'${String(str).replace(/'/g, "''")}'`;
}

// Map ISO 639-2/T codes → readable names for the most common ones
const LANG_MAP = {
  eng: 'English', fre: 'French', ger: 'German', spa: 'Spanish',
  por: 'Portuguese', jpn: 'Japanese', kor: 'Korean', ita: 'Italian',
  dut: 'Dutch', ara: 'Arabic', chi: 'Chinese', rus: 'Russian',
  pol: 'Polish', swe: 'Swedish', nor: 'Norwegian', dan: 'Danish',
  fin: 'Finnish', tur: 'Turkish', heb: 'Hebrew', hun: 'Hungarian',
};

function readableLanguage(codes) {
  if (!codes?.length) return null;
  // Prefer English edition; otherwise use first
  const eng = codes.find(c => c === 'eng');
  const code = eng || codes[0];
  return LANG_MAP[code] || code.toUpperCase();
}

function bestIsbn13(isbns) {
  if (!isbns?.length) return null;
  const thirteens = isbns.filter(i => i.length === 13 && i.startsWith('978'));
  return thirteens[0] || isbns.find(i => i.length === 13) || null;
}

function bestPublisher(publishers) {
  if (!publishers?.length) return null;
  // Skip "Unknown" or very short strings
  return publishers.find(p => p.length > 3 && !p.toLowerCase().includes('unknown')) || publishers[0];
}

async function fetchOLMetadata(title, authorLastName) {
  const t = encodeURIComponent(title);
  const a = encodeURIComponent(authorLastName);
  const url = `https://openlibrary.org/search.json?title=${t}&author=${a}&limit=5`
    + `&fields=title,number_of_pages_median,isbn,publisher,first_publish_year,language`;

  try {
    const res = await fetch(url, {
      headers: { 'User-Agent': 'IndieConverters/1.0 (tongeche@gmail.com)' },
    });
    if (!res.ok) return null;
    const data = await res.json();
    const docs = data.docs || [];

    const tLower = title.toLowerCase();
    const best = docs.find(d => d.title?.toLowerCase() === tLower)
      || docs.find(d => d.title?.toLowerCase().startsWith(tLower.slice(0, 6)))
      || docs[0];

    if (!best) return null;

    return {
      pubYear:    best.first_publish_year || null,
      pageCount:  best.number_of_pages_median || null,
      isbn13:     bestIsbn13(best.isbn),
      language:   readableLanguage(best.language),
      publisher:  bestPublisher(best.publisher),
    };
  } catch {
    return null;
  }
}

async function main() {
  const { data: books, error } = await supabase
    .from('books').select(`
      slug, title,
      books_authors ( position, authors ( display_name ) )
    `)
    .eq('is_published', true)
    .order('created_at');

  if (error) { console.error(error.message); process.exit(1); }
  console.log(`\nFetching metadata for ${books.length} books via Open Library…\n`);

  const lines = [
    '-- Auto-generated by scripts/enrich-metadata.mjs',
    '-- Populates pub_year, page_count, isbn_13, language, publisher_name.',
    '',
  ];

  let found = 0;

  for (const book of books) {
    const authorEntry = [...(book.books_authors || [])]
      .sort((a, b) => (a.position ?? 1) - (b.position ?? 1))[0];
    const fullName   = authorEntry?.authors?.display_name || '';
    const lastName   = fullName.split(' ').slice(-1)[0];

    process.stdout.write(`  ${book.title.padEnd(48).slice(0, 48)} `);

    const m = await fetchOLMetadata(book.title, lastName);
    if (!m) { console.log('✗ not found'); await sleep(250); continue; }

    const parts = [];
    if (m.pubYear)   parts.push(`pub_year = ${m.pubYear}`);
    if (m.pageCount) parts.push(`page_count = ${m.pageCount}`);
    if (m.isbn13)    parts.push(`isbn_13 = ${esc(m.isbn13)}`);
    if (m.language)  parts.push(`language = ${esc(m.language)}`);
    if (m.publisher) parts.push(`publisher_name = ${esc(m.publisher)}`);

    if (parts.length) {
      lines.push(`UPDATE public.books SET ${parts.join(', ')} WHERE slug = ${esc(book.slug)};`);
      found++;
    }

    console.log(
      `✓  ${m.pubYear || '?'}  ${String(m.pageCount || '?').padStart(4)}pp  ${(m.publisher || '').slice(0, 28)}`
    );
    await sleep(250);
  }

  const outPath = resolve(__dir, '../supabase/migrations/0013_book_metadata.sql');
  writeFileSync(outPath, lines.join('\n'), 'utf8');

  console.log(`\n─────────────────────────────────`);
  console.log(`Enriched : ${found} / ${books.length}`);
  console.log(`Written  → supabase/migrations/0013_book_metadata.sql`);
  console.log(`Next     : supabase db push`);
}

main().catch(e => { console.error(e); process.exit(1); });
