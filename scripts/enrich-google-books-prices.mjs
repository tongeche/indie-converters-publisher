/**
 * Phase 2 of price comparison: cross-checks Google Books' own sale price
 * (free, via the Google Books API — no scraping, no Amazon Associates
 * bootstrap problem) for every published book that has a real ISBN-13, and
 * upserts a 'google-books' retailer link tagged source = 'google_books' so
 * the UI can label it "Verified" instead of "entered by the author."
 *
 * This never touches author-declared retailer links (different retailer_id,
 * different source) — see replaceRetailerLinks() in src/lib/api.js.
 *
 * Run:  node scripts/enrich-google-books-prices.mjs
 * Then: apply the generated migration (supabase db push, or in this repo's
 *       current workflow: supabase db query --linked -f <generated file>)
 */

import { createClient } from '@supabase/supabase-js';
import { writeFileSync, readFileSync } from 'fs';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const env = Object.fromEntries(
  readFileSync(resolve(__dir, '../.env'), 'utf8')
    .split('\n').filter(l => l.includes('='))
    .map(l => l.split('=').map(s => s.trim()))
);

const supabase = createClient(env['VITE_SUPABASE_URL'], env['VITE_SUPABASE_ANON_KEY']);
const API_KEY  = env['GOOGLE_BOOKS_API_KEY'];
const sleep    = ms => new Promise(r => setTimeout(r, ms));

function esc(str) {
  if (str == null) return 'NULL';
  return `'${String(str).replace(/'/g, "''")}'`;
}

async function fetchGoogleBooksSaleInfo(isbn13) {
  const url = `https://www.googleapis.com/books/v1/volumes?q=isbn:${isbn13}&key=${API_KEY}`;
  const res = await fetch(url);
  if (!res.ok) { console.error(`  HTTP ${res.status}`); return null; }
  const data = await res.json();
  const item = data.items?.[0];
  if (!item) return null;

  const saleInfo = item.saleInfo;
  if (!saleInfo || saleInfo.saleability !== 'FOR_SALE' || !saleInfo.retailPrice?.amount) return null;

  return {
    price: saleInfo.retailPrice.amount,
    currency: saleInfo.retailPrice.currencyCode || 'USD',
    buyLink: saleInfo.buyLink || item.volumeInfo?.canonicalVolumeLink || null,
  };
}

async function main() {
  const { data: books, error } = await supabase
    .from('books')
    .select('id, slug, title, isbn_13')
    .eq('is_published', true)
    .not('isbn_13', 'is', null);

  if (error) { console.error(error.message); process.exit(1); }
  if (!books.length) { console.log('No published books with an ISBN-13 yet.'); return; }

  console.log(`\nCross-checking Google Books prices for ${books.length} books with a real ISBN…\n`);

  const lines = [
    '-- Auto-generated by scripts/enrich-google-books-prices.mjs',
    "-- Upserts source = 'google_books' retailer links; never touches author-declared rows.",
    '',
    "insert into public.retailers (slug, label) values ('google-books', 'Google Books') on conflict (slug) do nothing;",
    '',
  ];

  let found = 0;

  for (const book of books) {
    const isbn = book.isbn_13.replace(/[^0-9Xx]/g, '');
    process.stdout.write(`  ${book.title.padEnd(50).slice(0, 50)} `);

    const sale = await fetchGoogleBooksSaleInfo(isbn);

    if (sale && sale.buyLink) {
      lines.push(
        `insert into public.book_retailer_links (book_id, retailer_id, url, price, currency, source, price_updated_at)`,
        `select b.id, r.id, ${esc(sale.buyLink)}, ${sale.price}, ${esc(sale.currency)}, 'google_books', now()`,
        `from public.books b, public.retailers r`,
        `where b.slug = ${esc(book.slug)} and r.slug = 'google-books'`,
        `on conflict (book_id, retailer_id) do update set`,
        `  url = excluded.url, price = excluded.price, currency = excluded.currency,`,
        `  source = 'google_books', price_updated_at = now();`,
        '',
      );
      found++;
      console.log(`✓ $${sale.price} ${sale.currency}`);
    } else {
      console.log('✗ not for sale on Google Play');
    }

    await sleep(300);
  }

  const outPath = resolve(__dir, '../supabase/migrations/0060_google_books_price_check.sql');
  writeFileSync(outPath, lines.join('\n'), 'utf8');

  console.log(`\n─────────────────────────────────`);
  console.log(`Verified prices found : ${found} / ${books.length}`);
  console.log(`Written  → supabase/migrations/0060_google_books_price_check.sql`);
  console.log(`Next     : supabase db push`);
  console.log(`\nRe-run this script periodically (e.g. monthly) to refresh prices — safe to run repeatedly, it only upserts source = 'google_books' rows.`);
}

main().catch(e => { console.error(e); process.exit(1); });
